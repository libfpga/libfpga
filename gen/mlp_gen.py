#!/usr/bin/env python3
"""Generate a pipelined fixed-point MLP inference core from a JSON spec.

The library-grade generalization of github.com/libfpga/fpga-neuron: give
it a network (weights already trained + quantized to Q4.4 integers) and
it emits a synthesizable Verilog core plus a self-checking testbench that
compares the RTL against this script's own bit-exact model on every
supplied test vector.

Spec JSON:
{
  "name": "mlp",
  "in_frac": 4, "acc_frac": 8,          # Q formats
  "layers": [                            # each: weights[out][in], bias[out], act
    {"w": [[..],[..]], "b": [..], "act": "relu"},
    {"w": [[..]],      "b": [..], "act": "none"}
  ],
  "tests": [[x0, x1, ...], ...]          # inputs in Q(in_frac), integers
}

Usage:  python3 gen/mlp_gen.py spec.json outdir/
Emits:  outdir/<name>.v  and  outdir/tb_<name>.v
"""
import json
import os
import sys

SAT_HI, SAT_LO = 127, -128


def resize(v, from_f, to_f):
    """Round-half-up + saturate to 8-bit Q(to_f) — matches lfpga_fix_resize."""
    sh = from_f - to_f
    if sh > 0:
        v = (v + (1 << (sh - 1))) >> sh
    else:
        v = v << (-sh)
    return max(SAT_LO, min(SAT_HI, v))


def model(spec, x):
    """Bit-exact software model of the generated core."""
    inf, accf = spec["in_frac"], spec["acc_frac"]
    a = list(x)                      # current layer inputs, Q(inf)
    for layer in spec["layers"]:
        out = []
        for o, wrow in enumerate(layer["w"]):
            acc = sum(wrow[i] * a[i] for i in range(len(a)))   # Q(inf+4)
            acc += layer["b"][o] << inf                        # bias Q4.4<<inf
            # widen to accf fraction bits
            acc = acc << (accf - (inf + 4)) if accf > inf + 4 else \
                  acc >> ((inf + 4) - accf)
            if layer["act"] == "relu":
                acc = max(0, acc)
            elif layer["act"] == "leaky":
                acc = acc if acc >= 0 else (acc >> 3)
            out.append(resize(acc, accf, inf))
        a = out
    return a


def gen_verilog(spec):
    name, inf, accf = spec["name"], spec["in_frac"], spec["acc_frac"]
    n_in = len(spec["layers"][0]["w"][0])
    n_out = len(spec["layers"][-1]["w"])
    L = [f"// {name}: pipelined fixed-point MLP, generated by "
         "libfpga gen/mlp_gen.py",
         "// https://github.com/libfpga/libfpga  SPDX: MIT",
         f"module {name} (",
         "    input  wire clk,",
         "    input  wire rst,",
         f"    input  wire signed [{n_in}*8-1:0] x_flat,   // Q{inf}.{inf} inputs",
         f"    output wire signed [{n_out}*8-1:0] y_flat", ");"]
    # unpack inputs
    L.append(f"    wire signed [7:0] a0 [0:{n_in-1}];")
    L.append("    genvar gi;")
    L.append("    generate for (gi=0; gi<%d; gi=gi+1) begin: ui" % n_in)
    L.append("        assign a0[gi] = x_flat[gi*8 +: 8];")
    L.append("    end endgenerate")
    prev, prev_n = "a0", n_in
    for li, layer in enumerate(spec["layers"]):
        no = len(layer["w"])
        cur = f"a{li+1}"
        L.append(f"    reg signed [7:0] {cur} [0:{no-1}];   // layer {li} out")
        for o in range(no):
            terms = " + ".join(
                f"$signed(8'sd0 + ({layer['w'][o][i]})) * {prev}[{i}]"
                for i in range(prev_n))
            biasw = accf
            L.append(f"    wire signed [31:0] acc_{li}_{o} = "
                     f"({terms}) + ({layer['b'][o]} <<< {inf});")
            act = layer["act"]
            if act in ("relu", "leaky", "none"):
                L.append(f"    wire signed [31:0] pa_{li}_{o} = "
                         + ("(acc_%d_%d >= 0) ? acc_%d_%d : (acc_%d_%d >>> 3);"
                            % (li,o,li,o,li,o) if act == "leaky"
                            else "(acc_%d_%d < 0) ? 32'sd0 : acc_%d_%d;"
                            % (li,o,li,o) if act == "relu"
                            else "acc_%d_%d;" % (li,o)))
            L.append(f"    wire signed [7:0] rs_{li}_{o};")
            L.append(f"    lfpga_fix_resize #(.IN_W(32), .IN_F({inf+4}), "
                     f".OUT_W(8), .OUT_F({inf})) "
                     f"u_rs_{li}_{o} (.din(pa_{li}_{o}), .dout(rs_{li}_{o}));")
        L.append("    always @(posedge clk) begin")
        for o in range(no):
            L.append(f"        {cur}[{o}] <= rst ? 8'sd0 : rs_{li}_{o};")
        L.append("    end")
        prev, prev_n = cur, no
    # pack outputs
    L.append("    genvar go;")
    L.append(f"    generate for (go=0; go<{n_out}; go=go+1) begin: uo")
    L.append(f"        assign y_flat[go*8 +: 8] = {prev}[go];")
    L.append("    end endgenerate")
    L.append("endmodule")
    return "\n".join(L)


def gen_tb(spec):
    name, inf = spec["name"], spec["in_frac"]
    n_in = len(spec["layers"][0]["w"][0])
    n_out = len(spec["layers"][-1]["w"])
    depth = len(spec["layers"])   # one pipeline reg per layer
    L = [f"// Self-checking TB for {name}, generated by mlp_gen.py.",
         "`timescale 1ns/1ps",
         f"module tb_{name};",
         "    reg clk = 0, rst = 1;",
         f"    reg  signed [{n_in}*8-1:0] x_flat;",
         f"    wire signed [{n_out}*8-1:0] y_flat;",
         "    integer errors = 0, t;",
         f"    {name} dut (.clk(clk), .rst(rst), .x_flat(x_flat), .y_flat(y_flat));",
         "    always #5 clk = ~clk;",
         "    initial begin",
         f'        $dumpfile("wave.vcd"); $dumpvars(0, tb_{name});',
         "        repeat (2) @(negedge clk); rst = 0;"]
    for t, x in enumerate(spec["tests"]):
        y = model(spec, x)
        xf = "".join(f"{(v & 0xff):02x}" for v in reversed(x))
        L.append(f"        @(negedge clk); x_flat = {n_in*8}'h{xf};")
        L.append(f"        repeat ({depth}) @(negedge clk); #1;   // pipeline fill")
        for o in range(n_out):
            lit = (f"-8'sd{-y[o]}" if y[o] < 0 else f"8'sd{y[o]}")
            L.append(f"        if ($signed(y_flat[{o}*8 +: 8]) !== "
                     f"({lit})) begin errors=errors+1; "
                     f'$display("FAIL t{t} out{o}: got %0d want {y[o]}", '
                     f"$signed(y_flat[{o}*8 +: 8])); end")
    L += ['        if (errors == 0) $display("TB PASS: %s");' % name,
          '        else $display("TB FAIL: %s (%%0d errors)", errors);' % name,
          "        $finish;",
          "    end",
          "endmodule"]
    return "\n".join(L)


if __name__ == "__main__":
    if len(sys.argv) != 3:
        sys.exit("usage: mlp_gen.py spec.json outdir/")
    spec = json.load(open(sys.argv[1]))
    outdir = sys.argv[2]
    os.makedirs(outdir, exist_ok=True)
    open(os.path.join(outdir, f"{spec['name']}.v"), "w").write(
        gen_verilog(spec) + "\n")
    open(os.path.join(outdir, f"tb_{spec['name']}.v"), "w").write(
        gen_tb(spec) + "\n")
    print(f"generated {spec['name']}.v + tb_{spec['name']}.v in {outdir}")
    for x in spec["tests"]:
        print(f"  model {x} -> {model(spec, x)}")
